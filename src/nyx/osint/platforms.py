    def _load_platforms_from_json(self) -> int:
        """Load additional platforms from JSON data files.

        This method loads the remaining 1300+ platforms from Maigret's database
        stored in external JSON files, keeping the codebase maintainable while
        supporting the complete 2000+ platform catalog.
        """
        import json
        from pathlib import Path

        loaded_count = 0

        # Get the data directory path
        data_dir = Path(__file__).parent.parent.parent / "data" / "platforms"

        if not data_dir.exists():
            logger.warning(f"Platform data directory not found: {data_dir}")
            return 0

        # Load platforms from JSON files
        json_files = [
            "maigret_extended_platforms.json",
            "maigret_international_platforms.json",
            "maigret_niche_platforms.json",
            "custom_platforms.json",
        ]

        for json_file in json_files:
            file_path = data_dir / json_file
            if file_path.exists():
                try:
                    with open(file_path, 'r', encoding='utf-8') as f:
                        platforms_data = json.load(f)
                        loaded_count += self.merge_from_dict(platforms_data)
                        logger.info(f"Loaded {len(platforms_data)} platforms from {json_file}")
                except Exception as e:
                    logger.error(f"Error loading platforms from {json_file}: {e}")

        if loaded_count > 0:
            logger.info(f"Loaded {loaded_count} additional platforms from JSON files")

        return loaded_count


